#include "ft_printf.h"

int	put_nb(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
		return (write(1, "-2147483648", 11));
	if (n < 0)
	{
		len += put_c('-');
		n = -n;
	}
	if (n > 9)
	{
		len += put_nb(n / 10);
		len += put_nb(n % 10);
	}
	else
		len += put_c(n + 48);
	return (len);
}
