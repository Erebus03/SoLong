#include "ft_printf.h"

int	put_s(char *s)
{
	int	len;

	len = 0;
	if (s == NULL)
		return (write(1, "(null)", 6));
	while (*s)
	{
		len += write(1, s, 1);
		s++;
	}
	return (len);
}
