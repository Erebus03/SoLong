#include "ft_printf.h"

int	put_unb(unsigned int n)
{
	int	len;

	len = 0;
	if (n > 9)
	{
		len = put_unb(n / 10);
		len += put_unb(n % 10);
	}
	else
		len = put_c(n + 48);
	return (len);
}
